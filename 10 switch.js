var number = 4;

switch (number) {
    case 1:
        console.log("Number is 1");
        break;
    case 2:
        console.log("Number is 2");
        break;
    case 3:
    case 4:
        console.log("Number is 3");
        break;
    default:
        console.log("Number is not 1, 2, or 3");
        break;
}
